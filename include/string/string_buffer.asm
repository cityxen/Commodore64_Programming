#importonce

//////////////////////////////////////////////////////////////////
// reusable string buffer data
strbuf:
    .fill 256,0
buf_crsr:
    .byte 0

//////////////////////////////////////////////////////////////////
// zero string buffer

zero_strbuf:
    lda #$00
    ldx #$00
!:
    sta strbuf,x
    inx
    bne !-
    stx buf_crsr
    rts
